package dicision_making_statement;

public class Class_003 {

	//3rd type if_else_if or if_ladder statement;
	
	public static void main(String[] args) {
		double h = 400.98;
		
		if(h>400) {
			System.out.println("Condition 1 is true");
		}else if (h<400) {
			System.out.println("condition 2 is true");
		}else if(h>100) {
			System.out.println("condition 3 is true");
		}else if (h<700) {
			System.out.println("condition 4 is true");
		}
		else
		{ 
			System.out.println("all condition is false");
		}

	}

}
