package dicision_making_statement;

public class Switch_statement_001 {

	public static void main(String[] args) {
		int i= 25;
	    
		switch (i){
		case 1:
			System.out.println("Case - 1");
			break;
		case 25:
			System.out.println("case - 25");
			break;
		case 30:
			System.out.println("case - 30");
			break;
		    default:
		    	System.out.println("Your all cases is false");
		    break;	
		}
		
	}

}
