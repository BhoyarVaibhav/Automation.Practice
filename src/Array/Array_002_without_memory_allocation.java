package Array;

public class Array_002_without_memory_allocation {

	public static void main(String[] args) {
		
		//Array without memory allocation
		int[]quantity = {1,2,3,4,5,6,7,8,};
        System.out.println(quantity.length);
        
	    //print 1 value
		System.out.println(4);
		
		//to print all values
		for(int h = 0;h<8;h++) {
          System.out.println(quantity[h]);
      }
	}
	}
