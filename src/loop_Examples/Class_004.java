package loop_Examples;

public class Class_004 {

	public static void main(String[] args) {

		// Nested loop
		for(int x = 0; x< 10;x++) {
			System.out.println("The value of outer loop;-"+ x);
			for(int p = 0;p <5;p++) {
				System.out.println("The value of inner loop value;-"+ p);
			}
		}

	}

}
