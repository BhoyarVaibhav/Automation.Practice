package loop_Examples;

public class Class_001 {

	public static void main(String[] args) {
	
		for (int i = 0;i<5;i++) {
			System.out.println(i);
		}
          //print 1st 10 even number
		for(int c = 0;c<=10;c++) {
			System.out.println("The even num:-" + 2*c);
		}
		//print 1st 10 odd number
		for(int c = 0;c<=10;c++) {
			System.out.println("The odd num:-" + (2*c+1));
		}
	}

}
